QString CRandomizer::alphanumeric5() {
  QChar c1( 65 + _rng.bounded( 26 ) );
  QChar c2( 65+ _rng.bounded( 26 ) );
  int i = _rng.bounded( 1000 );

  return QStringLiteral( "%1%2%3" ).arg( c1 ).arg( c2 ).arg( paddedInt( i, 3 ) );
}


