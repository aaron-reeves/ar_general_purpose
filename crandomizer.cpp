QStringList CRandomizer::randList( const int length, const QStringList& sourceList ) {
  QSet<QString> usedStrs;
  QStringList result;

  Q_ASSERT( length <= sourceList.length() );

  int val;
  while( true ) {
    val = _rng.bounded( sourceList.length() );

    if( !usedStrs.contains( sourceList.at( val ) ) ) {
      usedStrs.insert( sourceList.at( val ) );
      result.append( sourceList.at( val ) );
      if( length == result.length() ) {
        break;
      }
    }
  }

  return result;
}


QStringList CRandomizer::randAlphanumeric5s( const int length ) {
  QSet<QString> usedStrs;
  QStringList result;

  while( true ) {
    QString val = this->alphanumeric5();
    if( !usedStrs.contains( val ) ) {
      usedStrs.insert( val );
      result.append( val );
      if( length == result.length() ) {
        break;
      }
    }
  }

  return result;
}


QList<int> CRandomizer::randInts( const int length, const int min, const int max ) {
  QSet<int> usedVals;
  QList<int> result;

  int val;
  while( true ) {
    val = _rng.bounded( min, max );
    if( !usedVals.contains( val ) ) {
      usedVals.insert( val );
      result.append( val );
      if( length == result.length() ) {
        break;
      }
    }
  }

  return result;
}



